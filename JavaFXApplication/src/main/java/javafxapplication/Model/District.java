package javafxapplication.Model;

public class District {

    private long id;
    private String name;

    protected District(){}

    public District(String name){
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
